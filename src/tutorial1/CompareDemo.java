package tutorial1;

public class CompareDemo {
	public static void main(String[] args) {
		
		int x = 6; 
		int y = 7; 
		int z = 10; 
		// > < == >= <= !=
		
		boolean compare = x > y; 	
		System.out.println(compare); 
		
		compare = x >= y; 
		System.out.println(compare); 
     
		compare = x < y; 
		System.out.println(compare); 
		
		compare = x <= y; 
		System.out.println(compare); 
		
		compare = x == y; 
		System.out.println(compare); 
		
		compare = x != y; 
		System.out.println(compare); 
	}

}









